/*
 * dns_server.h — DNS 服务器（头文件）
 * =====================================
 * 这是一个简易的 DNS 服务器，只做一件事：
 * 把所有域名解析到 ESP32 自己的 IP 地址（192.168.4.1）。
 *
 * 为什么需要 DNS？
 * 手机连上 ESP32 的 WiFi 热点后，通常会弹出系统通知（如 "登录WiFi网络"），
 * 或者手机浏览器打开任意网址时会触发 DNS 查询。
 * 我们把所有 DNS 请求都指向 ESP32 自己，手机就会弹出我们的配网页面。
 * 这个技术叫"Captive Portal"（强制门户）。
 */

void start_dns_server(void);
